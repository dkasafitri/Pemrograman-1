public class Latihan // nama class yg harus sama dengan nama filenya
{ //di bawah ini adalah method utama yang akan dieksekusi oleh java
	public static void main (String args[])
	{
		int firstNumber, secondNumber, results; //deklarasi variabel firstNumber, secondNumber dan results dengan tipe data integer
	
		firstNumber = 20; //pengisisan data variabel firstNumber
		secondNumber = 10; //pengisisan data variabel secondNumber
		
		results = firstNumber + secondNumber; //pengisian data variabel results dengan penjumlahan isi dari variabel firstNumber dan secondNumber
		System.out.println(results); //cetak isi variabel results
	}
}